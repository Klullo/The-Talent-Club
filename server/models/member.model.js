const mongoose = require('mongoose');

const Member = mongoose.model('Member', {
  firstName: String,
  lastName: String,
  email: String,
  username: String,
  password: String,
  isMember: Boolean,
  phoneNumber: String,
  jobTitle: String,
  hasPaid: Boolean,
  activatedSlack: Boolean,
  socialNetworks: [
      { name: String,
          url: String }
    ]
});

module.exports = Member;
